// mang[0]=9->num=10;->mang[1]=0->num=1;->mang[2]=.->mang[3]=1->num=0,count=4
void OLED_printFloat(float num){
	num = (float)num * 100;
	int8_t mang[] = {0,0,0,0,0};
	uint8_t count = 0;
	while(num != 0){
		mang[count] = num%10;
		num = num /10;
		++count;
		if(count==2) {mang[2]= -2; ++count;}
	}
	I2C_start();
	I2C_address(OLED_I2C_ADDR,I2C_DIRECTION_TX);
	I2C_write(OLED_DAT_MODE);
	while(count != 0){
		uint16_t offset = mang[count-1] + 16;
		offset += offset << 2;
		I2C_write(0x00);
		for(uint8_t i = 5; i;i--){ I2C_write(OLED_FONT[offset++]); }
		--count;
	}
	I2C_stop();		
}
void OLED_cursor(uint8_t xpos, uint8_t ypos)
{
	I2C_start();
	I2C_address(OLED_I2C_ADDR, I2C_DIRECTION_TX);
	I2C_write(OLED_CMD_MODE);
	I2C_write(xpos & 0x0F);
	I2C_write(0x10 | (xpos >> 4));
	I2C_write(0xB0 | (ypos & 0x07));
	I2C_stop();
}
void OLED_clear(void)
{
	OLED_cursor(0,0);
	I2C_start();
	I2C_address(OLED_I2C_ADDR, I2C_DIRECTION_TX);
	I2C_write(OLED_DAT_MODE);
	for (uint16_t i=0;i<512;i++)
	{
		I2C_write(0x00);
	}
	I2C_stop();
}